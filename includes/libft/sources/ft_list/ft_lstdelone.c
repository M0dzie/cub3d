#include "../../includes/libft.h"

/**
 * @brief The ft_lstdelone() function free the memory of element given by
 * argument using the function del than free.
 * 
 * @param t_list* lst 
 * @param *del 
 */
void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}
