#include "../../includes/libft.h"

/**
 * @brief The ft_lstiter() function iterate on the list lst and apply the
 * function f on each element.
 * 
 * @param t_list* lst 
 * @param *f 
 */
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst != NULL)
	{
		f(lst->content);
		lst = lst->next;
	}
}
